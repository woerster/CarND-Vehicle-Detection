# The Processing Pipline
def process_image(image, filename=None):

    # Find cars in given image / camera frame
    window_list = []
    scale = 1.0
    windows = find_cars(image, int(image.shape[0] * 0.5), int(image.shape[0] * 0.8), scale, svc, X_scaler, orient,
                            pix_per_cell, cell_per_block, spatial_size, hist_bins)
    window_list.extend(windows)
    if filename is not None:
        print(str(scale) + "-windows: " + str(len(windows)))

    scale = 2.0
    windows = find_cars(image, int(image.shape[0] * 0.5), int(image.shape[0] * 1.0), scale, svc, X_scaler, orient,
                            pix_per_cell, cell_per_block, spatial_size, hist_bins)
    window_list.extend(windows)
    if filename is not None:
        print(str(scale) + "-windows: " + str(len(windows)))

    # For test image processing draw all hot windows
    if filename is not None:
        draw_img = draw_boxes(np.copy(image), window_list, color=(0, 0, 255), thick=6)
    else:
        draw_img = np.copy(image)

    # Store current list of hot windows
    prev_hot_windows.append(window_list)

    # Add heat for the last recorded camera frames
    heat = np.zeros_like(image[:, :, 0]).astype(np.float)
    for frame_windows in prev_hot_windows:
        heat = add_heat(heat, frame_windows)

    # Apply threshold to help remove false positives
    if filename is not None:
        # Reduced threshold for test images
        threshold = 1
    else:
        threshold = 8
    heat = apply_threshold(heat, threshold)

    # Visualize the heatmap when displaying
    heatmap = np.clip(heat, 0, 255)

    # Find final boxes from heatmap using label function
    labels = label(heatmap)
    draw_img = draw_labeled_bboxes(draw_img, labels)

    # write output images for test image processing
    if (filename is not None):
        fig = plt.figure()
        plt.subplot(121)
        plt.imshow(draw_img)
        plt.title('Car Positions')
        plt.subplot(122)
        plt.imshow(heatmap, cmap='hot')
        plt.title('Heat Map')
        fig.tight_layout()

        plt.savefig('output_images/' + filename)

    return draw_img


